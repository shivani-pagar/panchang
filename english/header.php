<script>
    // Mobile Menu Toggle
    document.getElementById('mobile-menu-toggle').addEventListener('click', function(e) {
        e.stopPropagation();
        this.classList.toggle('active');
        document.getElementById('nav-menu').classList.toggle('active');
    });

    // Handle dropdowns on mobile
    document.querySelectorAll('.dropdown > .nav-link').forEach(item => {
        item.addEventListener('click', function(e) {
            if (window.innerWidth <= 992) {
                e.preventDefault();
                e.stopPropagation();
                const parent = this.parentElement;

                // Close other open dropdowns
                document.querySelectorAll('.dropdown.active').forEach(dropdown => {
                    if (dropdown !== parent) {
                        dropdown.classList.remove('active');
                    }
                });

                // Toggle current dropdown
                parent.classList.toggle('active');
            }
        });
    });

    // Handle nested dropdowns on mobile
    document.querySelectorAll('.dropdown-submenu > .dropdown-item').forEach(item => {
        item.addEventListener('click', function(e) {
            if (window.innerWidth <= 992) {
                e.preventDefault();
                e.stopPropagation();
                const parent = this.parentElement;

                // Close other open nested dropdowns
                const siblings = parent.parentElement.querySelectorAll('.dropdown-submenu.active');
                siblings.forEach(submenu => {
                    if (submenu !== parent) {
                        submenu.classList.remove('active');
                    }
                });

                // Toggle current nested dropdown
                parent.classList.toggle('active');
            }
        });
    });

    // Close mobile menu when clicking outside
    document.addEventListener('click', function(e) {
        const navMenu = document.getElementById('nav-menu');
        const menuToggle = document.getElementById('mobile-menu-toggle');

        if (window.innerWidth <= 992 &&
            navMenu.classList.contains('active') &&
            !navMenu.contains(e.target) &&
            !menuToggle.contains(e.target)) {
            navMenu.classList.remove('active');
            menuToggle.classList.remove('active');

            // Close all dropdowns
            document.querySelectorAll('.dropdown.active, .dropdown-submenu.active').forEach(el => {
                el.classList.remove('active');
            });
        }
    });

    // Add scroll effect to navbar
    window.addEventListener('scroll', function() {
        const navbar = document.querySelector('.navbar-container');
        if (window.scrollY > 50) {
            navbar.style.boxShadow = '0 5px 20px rgba(0, 0, 0, 0.08)';
        } else {
            navbar.style.boxShadow = '0 5px 25px var(--shadow)';
        }
    });

    // Close dropdowns when clicking on a dropdown item (for desktop)
    document.querySelectorAll('.dropdown-item').forEach(item => {
        item.addEventListener('click', function(e) {
            if (window.innerWidth > 992) {
                // Close all dropdowns when an item is clicked
                document.querySelectorAll('.dropdown, .dropdown-submenu').forEach(el => {
                    el.classList.remove('active');
                });
            }
        });
    });
</script>

